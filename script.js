const root = document.documentElement;
const langToggle = document.querySelector("#langToggle");
const themeToggle = document.querySelector("#themeToggle");
const translatable = document.querySelectorAll("[data-i18n]");
const cursorGlow = document.querySelector(".cursor-glow");
const canvas = document.querySelector("#particleCanvas");
const ctx = canvas.getContext("2d");

let currentLang = localStorage.getItem("yang-lang") || "zh";
let particles = [];
let pointer = { x: window.innerWidth / 2, y: window.innerHeight / 2 };

function setLanguage(lang) {
  currentLang = lang;
  root.lang = lang === "zh" ? "zh-Hant" : "en";
  translatable.forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = content[lang][key];
  });
  langToggle.textContent = lang === "zh" ? "EN" : "\u4e2d";
  localStorage.setItem("yang-lang", lang);
  animateLanguageSwap();
}

function setTheme(theme) {
  root.dataset.theme = theme;
  localStorage.setItem("yang-theme", theme);
}

function animateLanguageSwap() {
  if (!window.gsap) return;
  gsap.fromTo("[data-i18n]", {
    y: 12,
    opacity: 0
  }, {
    y: 0,
    opacity: 1,
    duration: 0.55,
    stagger: 0.012,
    ease: "power3.out"
  });
}

function bootAnimations() {
  if (!window.gsap) return;
  gsap.from(".site-header", { y: -30, opacity: 0, duration: 0.8, ease: "power3.out" });
  gsap.from(".hero-copy > *", { y: 44, opacity: 0, duration: 0.9, stagger: 0.1, ease: "power3.out" });
  gsap.from(".hero-visual", { scale: 0.92, opacity: 0, rotate: -2, duration: 1.1, ease: "power3.out" });
  gsap.from(".signal-card", { y: 24, opacity: 0, duration: 0.9, delay: 0.45, stagger: 0.18, ease: "back.out(1.7)" });

  gsap.to(".signal-one", { y: -18, duration: 2.6, repeat: -1, yoyo: true, ease: "sine.inOut" });
  gsap.to(".signal-two", { y: 16, duration: 2.9, repeat: -1, yoyo: true, ease: "sine.inOut" });
  gsap.from(".award-card", {
    y: 28,
    opacity: 0,
    duration: 0.8,
    stagger: 0.1,
    ease: "power3.out"
  });
  gsap.to(".skill-cloud span", {
    y: () => gsap.utils.random(-14, 14),
    x: () => gsap.utils.random(-10, 10),
    rotate: () => gsap.utils.random(-4, 4),
    duration: () => gsap.utils.random(1.8, 3.2),
    repeat: -1,
    yoyo: true,
    ease: "sine.inOut",
    stagger: 0.08
  });
  gsap.to(".orbital-ui", { filter: "hue-rotate(80deg)", duration: 4, repeat: -1, yoyo: true, ease: "sine.inOut" });
}

function resizeCanvas() {
  const ratio = window.devicePixelRatio || 1;
  canvas.width = window.innerWidth * ratio;
  canvas.height = window.innerHeight * ratio;
  canvas.style.width = `${window.innerWidth}px`;
  canvas.style.height = `${window.innerHeight}px`;
  ctx.setTransform(ratio, 0, 0, ratio, 0, 0);
  createParticles();
}

function createParticles() {
  const count = Math.min(140, Math.floor((window.innerWidth * window.innerHeight) / 10000));
  particles = Array.from({ length: count }, () => ({
    x: Math.random() * window.innerWidth,
    y: Math.random() * window.innerHeight,
    vx: (Math.random() - 0.5) * 0.55,
    vy: (Math.random() - 0.5) * 0.55,
    radius: Math.random() * 2.2 + 0.6,
    hue: [191, 315, 47][Math.floor(Math.random() * 3)]
  }));
}

function drawParticles() {
  ctx.clearRect(0, 0, window.innerWidth, window.innerHeight);

  particles.forEach((particle, index) => {
    const dx = pointer.x - particle.x;
    const dy = pointer.y - particle.y;
    const distance = Math.hypot(dx, dy);

    if (distance < 150) {
      particle.x -= dx * 0.004;
      particle.y -= dy * 0.004;
    }

    particle.x += particle.vx;
    particle.y += particle.vy;

    if (particle.x < 0 || particle.x > window.innerWidth) particle.vx *= -1;
    if (particle.y < 0 || particle.y > window.innerHeight) particle.vy *= -1;

    ctx.beginPath();
    ctx.fillStyle = `hsla(${particle.hue}, 100%, 68%, 0.62)`;
    ctx.arc(particle.x, particle.y, particle.radius, 0, Math.PI * 2);
    ctx.fill();

    for (let j = index + 1; j < particles.length; j += 1) {
      const other = particles[j];
      const linkDistance = Math.hypot(particle.x - other.x, particle.y - other.y);
      if (linkDistance < 105) {
        ctx.beginPath();
        ctx.strokeStyle = `rgba(120, 220, 255, ${0.13 - linkDistance / 1000})`;
        ctx.lineWidth = 1;
        ctx.moveTo(particle.x, particle.y);
        ctx.lineTo(other.x, other.y);
        ctx.stroke();
      }
    }
  });

  requestAnimationFrame(drawParticles);
}

langToggle.addEventListener("click", () => {
  setLanguage(currentLang === "zh" ? "en" : "zh");
});

themeToggle.addEventListener("click", () => {
  setTheme(root.dataset.theme === "dark" ? "light" : "dark");
});

window.addEventListener("pointermove", (event) => {
  pointer = { x: event.clientX, y: event.clientY };
  cursorGlow.style.transform = `translate(${event.clientX - 192}px, ${event.clientY - 192}px)`;
});

window.addEventListener("resize", resizeCanvas);

setTheme(localStorage.getItem("yang-theme") || "dark");
setLanguage(currentLang);
resizeCanvas();
drawParticles();
bootAnimations();
